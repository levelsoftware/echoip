func TestGetTokenKeyWithECDSAKey(t *testing.T) {
	err := ParseJWT(&config.Config{
		Debug: true,
		Jwt: config.Jwt{
			SigningMethod: "ES256",
			PublicKeyData: []byte(ecdsaPublicKey),
		},
	}, ecdsaToken)

	assert.Nil(t, err)
}

